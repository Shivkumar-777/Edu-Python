# Assignment Operators

num1 = int(input("Enter first number : "))
num2 = int(input("Enter second number : "))

num1 += num2
print(f"Update 1 : num1 : {num1}")

num1 -= num2
print(f"Update 2 : num1 : {num1}")

num1 *= num2
print(f"Update 3 : num1 : {num1}")

num1 /= num2
print(f"Update 4 : num1 : {num1}")

num1 //= num2
print(f"Update 5 : num1 : {num1}")